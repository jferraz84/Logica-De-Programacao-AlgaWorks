package capitulo03;

public class TiposPrimitivos {

	public static void main(String[] args) {

		byte variavelByte = 1;  // armazena numero de -128 á 127
		
		short variavelShort = 1;  // armazena numero de -32768 á 32767
		
		int variavelInteger = 1;  // armazena numero de -2147483648 á 2147483647
		
		long variavelLong = 4L;   // armazena numero de -9,223372037×10¹⁸ á   9,223372037×10¹⁸
		
		float variavelFloat = 1.0f;
		
		double variavelDouble = 1.0;
		
		char variavelCharacter = 'A';
		
		boolean variavelBoolean = true;

	}

}
